
// Blog One
function showBlogOne() {
    var i = document.getElementById("blogPostOne");
    if (i.style.display === "none") {
      i.style.display = "flex";
    } else {
      i.style.display = "none";
    }
}

function hideBlogOne() {
    var i = document.getElementById("blogPostOne");
    if (i.style.display === "flex") {
      i.style.display = "none";
    } else {
      i.style.display = "flex";
    }
}

